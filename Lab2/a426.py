import ipaddress as ip


def a46(a):
    a4=ip.IPv4Interface(a)
    ah=f'{int(a4):08x}'
    
    return ip.IPv6Interface(f'fc00::{ah[:4]}:{ah[4:]}/{96+a4._prefixlen}')

ads = ['10.22.32.0/31','10.22.32.2/31','10.22.32.4/31',
       '10.22.32.64/31','10.22.32.66/31','10.22.32.68/31',
        '10.22.36.1/32','10.22.36.2/32',
       '10.22.37.1/32','10.22.37.2/32','10.22.37.3/32']


for a in ads:
    print(f"{a} -> {a46(a)}") 
    
    
